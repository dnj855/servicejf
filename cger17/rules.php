        <h2 class="card-title mb-2">Règlement du challenge handball mondial</h2>
            <div class="card border-info mb-3">
                <div class="card-body">
                    <ol>
                        <li class="h4">Durée et organisation du concours</li>
                        <p class="text-justify">Comme lors de l'UEFA Euro 2016, le service jeux&festivités: organise un grand concours de pronostics durant le championnat du monde de handball féminin.
                            Pour participer, il faut être membre du service jeux&festivités: et s'inscrire au concours avant le début de la compétition <strong>(soit le 1er décembre 2017)</strong>.</p>
                        <p class="text-justify">Comme l'intégralité de la compétition se déroule sur le portail exclusif du service jeux&festivités: (<a href="http://bit.ly/servicejf">http://bit.ly/servicejf</a>), c'est bien sur ce site qu'il faut se rendre pour valider son inscription.
                            Et c'est également sur le portail que le participant devra valider ses pronostics pour les différents matches retenus.</p>
                        <li class="h4">Matches à pronostiquer</li>
                        <p class="text-justify">Pour cette compétition, les organisateurs ont fait le choix de ne pas soumettre tous les matches aux pronostics des participants.
                            Seuls les matches de l'équipe de France et la finale seront proposés aux pronostics. Ainsi, il y aura au minimum 6 et au maximum 10 matches à pronostiquer durant toute la compétition.</p>
                        <p class="text-justify">Pour chaque match soumis à pronostic, chaque participant doit pronostiquer le score final <em>(à l'issue du temps réglementaire ou des prolongations s'il y en a)</em>.
                            En phase finale, s'il pronostique un match nul, le participant doit également préciser quelle équipe, selon lui, se qualifie pour le tour suivant à l'issue de la séance de tirs aux buts.</p>
                        <li class="h4">Barème des points</li>
                        <p class="text-justify">En fonction des résultats de l'équipe de France, il peut y avoir jusqu'à <strong>105 points</strong> mis en jeu dans ce challenge. Voici leur répartition :</p>
                        <ul>
                            <li><strong>Règle générale</strong><br/>
                                Chaque type de points peut être remporté indépendamment des autres. Pour le dire autrement, pas besoin d'avoir le bon 1N2 pour remporter les autres points.
                            </li>
                            <li>
                                <strong>Vainqueur final</strong><br/>
                                En cas de bon pronostic du vainqueur final, <em>5 points</em> seront attribués à la fin de la compétition.
                            </li>
                            <li>
                                <strong>En phase de poules</strong><br/>
                                <ul>
                                    <li>Pour chaque bon résultat (1N2) : <em>3 points</em>.</li>
                                    <li>En fonction de l'écart pronostiqué (cf. exemple plus bas) : <em>de 0 à 3 points</em>.</li>
                                    <li>Pour le score exact : <em>3 points bonus</em>.</li>
                                </ul>
                            </li>
                            <li>
                                <strong>En phase finale</strong><br/>
                                <ul>
                                    <li>Pour chaque bon résultat (1N2) : <em>3 points</em>.</li>
                                    <li>Pour chaque bon vainqueur : <em>2 points</em>.</li>
                                    <li>En fonction de l'écart pronostiqué (cf. exemple plus bas) : <em>de 0 à 3 points</em>.</li>
                                    <li>Pour le score exact : <em>3 points bonus</em>.</li>
                                </ul>
                            </li>
                            <li>
                                <strong>Règle des écarts</strong><br/>
                                <ul>
                                    <li>Pour un écart de 0 ou 1 : <em>3 points</em>.</li>
                                    <li>Pour un écart de 2 ou 3 : <em>2 points</em>.</li>
                                    <li>Pour un écart de 4 ou 5 : <em>1 point</em>.</li>
                                    <li>Pour tout écart supérieur : <em>0 point</em>.</li>
                                </ul>
                            </li>
                            <p class="bg-info rounded p-2 text-justify text-white"><em>Prenons l'exemple suivant : la France bat la Slovénie sur le score de 23-20. L'écart entre les deux scores est donc de 3.
                                    Le participant, lui, a pronostiqué une victoire de la France sur le score de 21-20. Chez lui, l'écart est de 1.
                                    L'écart entre les deux écarts est de 2, cela lui permet de remporter 2 points.</em></p>
                        </ul>
                        <li class="h4">Calendrier des pronostics</li>
                        <p class="text-justify">Le participant peut modifier son pronostic autant de fois qu'il le souhaite, jusqu'à la clôture de la phase de pronostics. Elle intervient systématiquement 1h avant le coup d'envoi du match concerné. Voici le détail :</p>
                        <ul class="mb-3">
                            <li><strong>2 décembre 17h :</strong> phase de poules - match 1 (France-Slovénie)</li>
                            <li><strong>3 décembre 17h :</strong> phase de poules - match 2 (Angola-France)</li>
                            <li><strong>5 décembre 19h30 :</strong> phase de poules - match 3 (France-Paraguay)</li>
                            <li><strong>7 décembre 19h30 :</strong> phase de poules - match 4 (Espagne-France)</li>
                            <li><strong>8 décembre 17h :</strong> phase de poules - match 5 (France-Roumanie)</li>
                            <li><strong>10 ou 11 décembre :</strong> 8e de finale <em>(le cas échéant)</em></li>
                            <li><strong>12 ou 13 décembre :</strong> quart de finale <em>(le cas échéant)</em></li>
                            <li><strong>15 décembre :</strong> demi-finale <em>(le cas échéant)</em></li>
                            <li><strong>17 décembre 13h30 :</strong> petite finale <em>(le cas échéant)</em></li>
                            <li><strong>17 décembre 16h30 :</strong> finale</li>
                        </ul>
                        <li class="h4">Recours et litiges</li>
                        <p class="text-justify">En cas de litige non prévu par le présent règlement, les organisateurs sont seuls juges et statuent sur tout recours qui leur serait soumis.</p>
                    </ol>
